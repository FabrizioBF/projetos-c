// 35.Calcule a área de um retângulo (multiplicando o comprimento e a largura)
#include <stdio.h>
int main(){

// criando variáveis do tipo inteiro
int comprimento = 4;
int largura = 6;
int area;

// calcular a área de um retângulo
area = comprimento * largura;

// imprimir os conteúdos das variáveis
printf("O comprimento é: %d\n", comprimento);
printf("A largura é: %d\n", largura);
printf("A área do retângulo é: %d", area);
return 0;
 }
