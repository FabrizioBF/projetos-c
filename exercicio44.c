// 44.Use o número do dia da semana para calcular e gerar o nome do dia da semana.
#include <stdio.h>

int main()
{
  int dia = 5;

switch (dia) {
  case 1:
 	printf("Segunda-feira");
	break;
  case 2:
 	printf("Terça-feira");
	break;
  case 3:
 	printf("Quarta-feira");
	break;
  case 4:
 	printf("Quinta-feira");
	break;
  case 5:
 	printf("Sexta-feira");
	break;
  case 6:
 	printf("Sábado");
	break;
  case 7:
 	printf("Sunday");
	break;
}

}
